export function getCustomer(id: string): Customer | undefined {
  return customers.find((c) => c.id === id);
}

export function findCustomerByPhone(phone: string): Customer | undefined {
  const normalized = phone.replace(/\D/g, "");
  return customers.find((c) => c.phone.replace(/\D/g, "") === normalized);
}

export function findCustomerByVatId(vatId: string): Customer | undefined {
  return customers.find((c) => c.vatId === vatId);
}

export function birthdayCustomersThisMonth(month = new Date().getMonth() + 1): Customer[] {
  return customers.filter((c) => {
    if (!c.birthday) return false;
    return new Date(c.birthday).getMonth() + 1 === month;
  });
}
